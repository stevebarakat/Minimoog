export type Preset = {
  id: string;
  name: string;
  description: string;
  category: string;
  controllers: {
    tune: number; // 0-10
    glideTime: number; // 0-10
    modMix: number; // 0-10
    osc3FilterEgSwitch: boolean;
    noiseLfoSwitch: boolean;
    oscillatorModulationOn: boolean; // Oscillator vibrato
    osc3Control: boolean; // Oscillator 3 keyboard control
    keyboardControl1: boolean; // Keyboard tracking 1
    keyboardControl2: boolean; // Keyboard tracking 2
  };
  filter: {
    filterCutoff: number;
    filterEmphasis: number;
    filterContourAmount: number;
    filterAttack: number;
    filterDecay: number;
    filterSustain: number;
    filterModulationOn: boolean; // Filter LFO modulation
  };
  loudness: {
    loudnessAttack: number;
    loudnessDecay: number;
    loudnessSustain: number;
  };
  oscillators: Partial<SynthState>;
  sidePanel: {
    glideOn: boolean;
    decaySwitchOn: boolean;
    lfoRate: number;
    lfoWaveform: "triangle" | "square";
    modWheel: number;
  };
  mainVolume: number;
  shareURL?: string; // Optional URL for sharing
};

